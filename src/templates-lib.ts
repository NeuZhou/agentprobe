/**
 * List all available templates.
 */
export function listTestTemplates(): TestTemplate[] {
  return Object.values(TEMPLATES);
}

/**
 * Get a template by name.
 */
export function getTestTemplate(name: string): TestTemplate | undefined {
  return TEMPLATES[name];
}

/**
 * Get template content by name, ready to write to file.
 */
export function getTemplateContent(name: string): string {
  const template = TEMPLATES[name];
  if (!template) {
    const available = Object.keys(TEMPLATES).join(', ');
    throw new Error(`Unknown template: "${name}". Available: ${available}`);
  }
  return template.content;
}

/**
 * List template names by category.
 */
export function listTemplatesByCategory(category: string): TestTemplate[] {
  return Object.values(TEMPLATES).filter(t => t.category === category);
}

/**
 * Check if a template exists.
 */
export function hasTemplate(name: string): boolean {
  return name in TEMPLATES;
}
